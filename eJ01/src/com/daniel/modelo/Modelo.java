package com.daniel.modelo;

public class Modelo {
	private int numero;
	private double nreal;
	private String cadena;
	private char unschar;

	public Modelo(){
		
	}
	
	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}

	public double getNreal() {
		return nreal;
	}

	public void setNreal(double nreal) {
		this.nreal = nreal;
	}

	public String getCadena() {
		return cadena;
	}

	public void setCadena(String cadena) {
		this.cadena = cadena;
	}

	public char getUnschar() {
		return unschar;
	}

	public void setUnschar(char unschar) {
		this.unschar = unschar;
	}
	
	
	
	
}
